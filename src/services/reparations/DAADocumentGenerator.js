    /**
     * Generate comprehensive DAA DOCX document
     */
    async generateDOCX(daaRecord, slaveholderData, debtCalculation, acknowledgerInfo) {
        const sections = [];

        // Title Page
        sections.push(
            ...this.createTitlePage(acknowledgerInfo, daaRecord)
        );

        // Table of Contents (placeholder)
        sections.push(
            new Paragraph({
                text: 'TABLE OF CONTENTS',
                heading: HeadingLevel.HEADING_1,
                alignment: AlignmentType.CENTER,
                spacing: { before: 400, after: 200 }
            }),
            new Paragraph({
                text: '[Table of Contents will be generated by Word]',
                alignment: AlignmentType.CENTER,
                spacing: { after: 400 }
            }),
            this.createPageBreak()
        );

        // Part I: Legal Framework
        sections.push(
            ...this.createLegalFramework(daaRecord, slaveholderData, debtCalculation)
        );

        // Article I: Acknowledgment of Debt
        sections.push(
            ...this.createDebtAcknowledgment(daaRecord, slaveholderData, debtCalculation)
        );

        // Article II: Payment Terms
        sections.push(
            ...this.createPaymentTerms(daaRecord, acknowledgerInfo)
        );

        // Article III: Escrow and Disbursement
        sections.push(
            ...this.createEscrowTerms()
        );

        // Article IV: Legal Effect and Waivers
        sections.push(
            ...this.createLegalWaivers()
        );

        // Article V: Annual Re-Petition
        sections.push(
            ...this.createPetitionTerms()
        );

        // Exhibits: Primary Sources
        sections.push(
            ...this.createExhibits(slaveholderData)
        );

        // Signature Page
        sections.push(
            ...this.createSignaturePage(acknowledgerInfo, daaRecord)
        );

        // Create document
        const doc = new Document({
            sections: [{
                properties: {},
                children: sections
            }]
        });

        // Generate filename
        const filename = `DAA-${daaRecord.agreementNumber}-${acknowledgerInfo.name.replace(/\s+/g, '_')}.docx`;
        const filepath = path.join(this.outputDir, filename);

        // Write to file
        const buffer = await Packer.toBuffer(doc);
        fs.writeFileSync(filepath, buffer);

        return filepath;
    }

