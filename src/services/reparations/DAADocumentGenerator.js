    /**
     * EXHIBIT E: Summary of Debts
     */
    createExhibitE(slaveholderData) {
        const sections = [
            this.createPageBreak(),
            new Paragraph({
                text: 'EXHIBIT E',
                heading: HeadingLevel.HEADING_1,
                spacing: { before: 400, after: 200 }
            }),
            new Paragraph({
                text: 'SUMMARY OF DEBTS',
                heading: HeadingLevel.HEADING_2,
                alignment: AlignmentType.CENTER,
                spacing: { after: 200 }
            })
        ];
        
        // Create summary table
        const tableRows = [
            new TableRow({
                children: [
                    new TableCell({ children: [new Paragraph({ text: 'Enslaved Person', bold: true })] }),
                    new TableCell({ children: [new Paragraph({ text: 'Slaveholder', bold: true })] }),
                    new TableCell({ children: [new Paragraph({ text: 'Debt Amount', bold: true })] })
                ]
            })
        ];
        
        let grandTotal = 0;
        
        for (const data of slaveholderData) {
            for (const person of data.enslavedPersons) {
                const years = person.years_enslaved || 30;
                const debt = years * 58620 * 596.63 * 3.2; // Simplified calculation
                grandTotal += debt;
                
                tableRows.push(
                    new TableRow({
                        children: [
                            new TableCell({ children: [new Paragraph(person.enslaved_name)] }),
                            new TableCell({ children: [new Paragraph(data.slaveholder.slaveholder_name)] }),
                            new TableCell({ children: [new Paragraph(`$${debt.toLocaleString('en-US', { maximumFractionDigits: 0 })}`)] })
                        ]
                    })
                );
            }
        }
        
        // Grand total row
        tableRows.push(
            new TableRow({
                children: [
                    new TableCell({ children: [new Paragraph({ text: 'GRAND TOTAL', bold: true })] }),
                    new TableCell({ children: [new Paragraph('')] }),
                    new TableCell({ children: [new Paragraph({ text: `$${grandTotal.toLocaleString('en-US', { maximumFractionDigits: 0 })}`, bold: true })] })
                ]
            })
        );
        
        sections.push(
            new Table({
                width: { size: 100, type: WidthType.PERCENTAGE },
                rows: tableRows
            }),
            new Paragraph({
                text: '',
                spacing: { after: 200 }
            }),
            new Paragraph({
                text: '─'.repeat(70),
                alignment: AlignmentType.CENTER,
                spacing: { after: 100 }
            }),
            new Paragraph({
                text: `TOTAL DEBT (Direct Line): $${grandTotal.toLocaleString('en-US', { maximumFractionDigits: 0 })}`,
                bold: true,
                alignment: AlignmentType.CENTER,
                spacing: { after: 100 }
            }),
            new Paragraph({
                text: '─'.repeat(70),
                alignment: AlignmentType.CENTER,
                spacing: { after: 200 }
            }),
            new Paragraph({
                text: 'Note: This total reflects the documented enslaved persons in Obligor\'s direct ancestral line. Per stirpes division among all descendants will be calculated upon execution of similar acknowledgments by other family members.',
                italics: true,
                spacing: { after: 100 }
            })
        );
        
        return sections;
    }

